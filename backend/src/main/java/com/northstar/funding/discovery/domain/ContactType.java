package com.northstar.funding.discovery.domain;

/**
 * Contact Type enumeration for Contact Intelligence
 * 
 * Categorizes different types of contacts in the funding ecosystem
 * for targeted relationship building and communication strategies.
 */
public enum ContactType {
    /**
     * Manages specific funding programs - primary decision support
     */
    PROGRAM_OFFICER,
    
    /**
     * General foundation personnel - organizational contacts
     */
    FOUNDATION_STAFF,
    
    /**
     * Government agency contacts - public sector funding
     */
    GOVERNMENT_OFFICIAL,
    
    /**
     * University/research contacts - academic funding sources
     */
    ACADEMIC_CONTACT,
    
    /**
     * Business/corporate foundation contacts - corporate social responsibility
     */
    CORPORATE_CONTACT
}
